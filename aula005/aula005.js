// > maior 
// < menor
// >= maior ou igual
// <=  menor ou igual
// == igual 
// == igual 
// = recebe
// != diferente do valor
// ! not não ou é true ou false

let num1 = 10;
let num2 = 5;
let num3 = 10;
console.log(num1 > num2)// true
console.log(num1 >= num3)// true
